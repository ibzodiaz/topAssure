    private void btnConnexionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConnexionActionPerformed
        if(btnConnexion.getText().equals("Connexion")){
            try {
                connect();

                Statement st = connection.createStatement();
                rs = st.executeQuery("SELECT * FROM login");
                while(rs.next()){
                    if(rs.getString("user").equalsIgnoreCase(txtUser.getText())  && rs.getString("password").equalsIgnoreCase(implode(txtPassword.getPassword())) ){

                        btnListClient.setEnabled(true);
                        btnProduction.setEnabled(true);
                        labelAuth.setText("Authentifi√© !");
                        labelAuth.setForeground(Color.green);
                        txtUser.setText("");
                        txtPassword.setText("");
                        btnConnexion.setText("D√©connexion");
                        txtUser.setEnabled(false);
                        txtPassword.setEnabled(false);
                        


                    }else{

                        labelAuth.setText("Identifiants Incorrects !!!");

                    }
                }

            } catch (SQLException ex) {
                Logger.getLogger(menu.class.getName()).log(Level.SEVERE, null, ex);
            }
        }else{
            btnListClient.setEnabled(false);
            btnProduction.setEnabled(false);
            labelAuth.setText("En attente d'authentification...");
            labelAuth.setForeground(Color.red);
            btnConnexion.setText("Connexion");
            txtUser.setEnabled(true);
            txtPassword.setEnabled(true);
            lc.setVisible(false);
            mn.setVisible(false);
        }
        
    }//GEN-LAST:event_btnConnexionActionPerformed

