    private void btnExportExcel2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExportExcel2ActionPerformed

        JFileChooser fchoose = new JFileChooser();

        int option = fchoose.showSaveDialog(ShowAttestClient.this);

        if(option == JFileChooser.APPROVE_OPTION){

            String name = fchoose.getSelectedFile().getName();

            String path = fchoose.getSelectedFile().getParentFile().getPath();

            String file = path+"\\"+name+".xls";

            try

            {

                TableModel m = jTableInfCli.getModel();

                FileWriter fw = new FileWriter(file);

                for(int i=0; i < m.getRowCount(); i++) {

                    for(int j=0; j < m.getColumnCount(); j++) {

                        fw.write(m.getValueAt(i,j) + "\t");

                    }

                    fw.write("\n");

                }

                fw.close();

            }

            catch(IOException e){

                System.out.println(e);

            }

            JOptionPane.showMessageDialog(null, "Fichier exporté !");

        }
    }//GEN-LAST:event_btnExportExcel2ActionPerformed
    
    public void Table3(){
        connect();
        int i= jTableClient.getSelectedRow();
        DefaultTableModel model0= (DefaultTableModel) jTableClient.getModel();
        
        jCode.setText(model0.getValueAt(i,1).toString());
        jAgence.setText(model0.getValueAt(i,2).toString());
        jLocalisation.setText(model0.getValueAt(i,3).toString());
        jNombreAttestation.setText(model0.getValueAt(i,4).toString());
        jTypeAttestation.setText(model0.getValueAt(i,5).toString());
        jDate.setText(model0.getValueAt(i,6).toString());
        
        
        String[] production ={"ATTESTATIONS NON PARVENUES"};

        String[] afficher = new String[1];
    
        DefaultTableModel model = new DefaultTableModel(null,production);
        try {

            connect();

            Statement st = connection.createStatement();

            rs = st.executeQuery("SELECT * FROM AttestClient WHERE "+model0.getValueAt(i,0).toString()+" = idClient");

           

            while(rs.next()){
                
                afficher[0] = rs.getString("numeros");                

                model.addRow(afficher);
                
                compteur++;
            }

            jTableInfCli.setModel(model);
            
         
            connection.close();

        } catch (Exception e) {

            e.printStackTrace();

        }finally{

            if(connection != null)

                try {

                        connection.close();

                } catch (SQLException ex) {

                    Logger.getLogger(main.class.getName()).log(Level.SEVERE, null, ex);

                }

        }
    }
    
