    public void UpdateAttestNum(){
        int i= jTableClient.getSelectedRow();
        DefaultTableModel model0= (DefaultTableModel) jTableClient.getModel();
        try {
            connect();
            pstmt = connection.prepareStatement("UPDATE clients SET nombre_attest = ? WHERE id = ?");
            pstmt.setInt(1,compteur);
            pstmt.setString(2,model0.getValueAt(i,0).toString());
            pstmt.executeUpdate();
            
            connection.close();
            jNombreAttestation.setText(""+compteur);
            compteur = 0;
        } catch (SQLException ex) {
            Logger.getLogger(ShowAttestClient.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
