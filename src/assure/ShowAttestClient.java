    private void btnDeleteIntervalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteIntervalActionPerformed

        if(txtAttest1.getText().equals("") || txtAttest2.getText().equals("") || jDateOperation.getDate() == null){

            JOptionPane.showMessageDialog(null,"Veuillez d'abord remplir les deux champs et la date !");

        }else{

            int a = Integer.parseInt(txtAttest1.getText());

            int b = Integer.parseInt(txtAttest2.getText());

            if( a > b ){

                JOptionPane.showMessageDialog(null,"Inversez les valeurs saisies !");

            }else{
                try {
                    Date date = new SimpleDateFormat("dd-MM-yyyy").parse(jDate.getText());
                    
                    if(jDateOperation.getDate().before(date)){
                        
                        JOptionPane.showMessageDialog(null,"ATTENTION! la date que vous essayez d'entrer est antérieure à la date d'enregistrement ");
                   
                    }else{
                        
                        int res = JOptionPane.showConfirmDialog(null, "Vous êtes sur le point de transférer les attestations parvenues !","Transfert",JOptionPane.WARNING_MESSAGE);

                        if(res == JOptionPane.YES_OPTION){


                                int k = jTableClient.getSelectedRow();
                                DefaultTableModel model= (DefaultTableModel) jTableClient.getModel();


                                try {

                                    connect();
                                    Date dateFromDateChooser;
                                    String dateString;

                                    stmt = connection.createStatement();

                                    pstmt = connection.prepareStatement("INSERT INTO attestationParvenue(idTheClient,numero,coming_date) VALUES(?,?,?);" );
                                    pstmt.setString(1,model.getValueAt(k,0).toString());
                                    for(int i = a ; i <= b  ; i++){
                                        pstmt.setInt(2,i);
                                        dateFromDateChooser = jDateOperation.getDate();
                                        dateString = String.format("%1$td-%1$tm-%1$tY", dateFromDateChooser);
                                        pstmt.setString(3,dateString);
                                        if(i < b){
                                            pstmt.executeUpdate();
                                        }  
                                    }

                                    pstmt.executeUpdate();


                                    stmt.executeUpdate("DELETE FROM AttestClient WHERE numeros >="+a+" AND numeros <="+b+" AND idClient = "+model.getValueAt(k,0).toString()+";" );

                                    connection.close();



                                } catch (Exception e) {

                                    e.printStackTrace();

                                }



                        }
                    }
                    
                } catch (ParseException ex) {
                    Logger.getLogger(ShowAttestClient.class.getName()).log(Level.SEVERE, null, ex);
                }
 
            }

        }
        Table3();
        UpdateAttestNum();
    }//GEN-LAST:event_btnDeleteIntervalActionPerformed
    
    
